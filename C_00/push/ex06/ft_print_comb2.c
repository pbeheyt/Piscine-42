#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	c;

	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	c = '0' + nb % 10;
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	while (a <= 99)
	{
		b = a + 1;
		while (b <= 99)
		{
			if (a <= 9)
				write(1, "0", 1);
			ft_putnbr(a);
			write(1, " ", 1);
			if (b <= 9)
				write(1, "0", 1);
			ft_putnbr(b);
			if ((a != 98) || (b != 99))
				write(1, ", ", 2);
		b++;
		}
	a++;
	}
}
