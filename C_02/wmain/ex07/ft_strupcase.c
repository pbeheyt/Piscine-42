char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a') && (str[i] <= 'z'))
			str[i] = str[i] - 32;
		i++;
	}
	return (str);
}

void 	main(void)
{
	char	text1[] = "gfghdj";
	char	text2[] = "jfhf";
	ft_strupcase(text1);	
}
