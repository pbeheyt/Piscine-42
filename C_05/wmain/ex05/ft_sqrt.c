#include<stdio.h>

int        ft_sqrt(int nb)
{
    int i;
    int res;

    i = 0;
    while (i * i < nb && i < 46340)
        i++;
    if (i * i == nb)
        res = i;
    else
        res = 0;
    return (res);
}

